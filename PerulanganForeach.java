package j8perulanganwhile;
public class PerulanganForeach {
    public static void main(String[] args) {
        
        //membuat array
        int angka[] = {3,1,42,24,12};
        
        //menggunakan perulangan For each untuk manampilkan angka
        for( int x : angka ){
            System.out.println(x + " ");
            
        }
    }
    
}
